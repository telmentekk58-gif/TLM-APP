import streamlit as st
from google import genai

# 1. Google Gemini AI холболт
# Түрүүний хуулж авсан урт API түлхүүрээ доорх "ЯГ ЭНД ХУУЛЖ ТАВИАРАЙ" гэсэн текстийн оронд тавиарай!
API_KEY = "ЯГ ЭНД ХУУЛЖ ТАВИАРАЙ"

client = genai.Client(api_key=API_KEY)

# 2. Вэбсайтны толгой хэсэг
st.set_page_config(page_title="AI Career Guide", page_icon="🎯")
st.title("🎯 Ирээдүйн Мэргэжил Сонголтын AI Туслах")
st.write("Таны сонирхол, давуу талд тулгуурлан хамгийн ирээдүйтэй мэргэжлийг Gemini AI бодож олно.")

# 3. Хэрэглэгчийн оролт
нэр = st.text_input("Таны нэр хэн бэ?", placeholder="Энд нэрээ бичнэ үү...")
сонирхол = st.text_input("Та юу сонирхдог вэ? (Жишээ нь: зураг зурах, тоглоом тоглох, математик, спорт...)", placeholder="Сонирхлоо дэлгэрэнгүй бичвэл AI илүү гоё хариулна...")

# 4. AI ажиллах хэсэг
if st.button("Ирээдүйг шинжлэх 🧠"):
    if нэр and сонирхол:
        with st.spinner("Gemini AI танд зориулж мэргэжил сонголтыг тооцоолж байна..."):
            try:
                # AI-д өгөх даалгавар (Prompt)
                даалгавар = f"""
                Хэрэглэгчийн нэр: {нэр}
                Хэрэглэгчийн сонирхол: {сонирхол}
                
                Дээрх мэдээлэлд тулгуурлан Монгол хэлээр маш сонирхолтой, урам зориг өгсөн хариулт бэлдэнэ үү.
                Дараах бүтцээр хариулна уу:
                1. {нэр}-д урам зориг өгсөн мэндчилгээ.
                2. Түүний сонирхолд тохирох ирээдүйн хамгийн эрэлттэй 1-2 мэргэжлийн нэр, тайлбар.
                3. Тэр мэргэжлээр амжилтанд хүрэхийн тулд одооноос юу сурах хэрэгтэй вэ (Зөвлөгөө).
                """
                
                # Gemini модель руу илгээх
                response = client.models.generate_content(
                    model='gemini-2.5-flash',
                    contents=даалгавар,
                )
                
                # Хариултыг дэлгэцэнд гоёор харуулах
                st.balloons()
                st.success(f"✨ {нэр}-д зориулсан AI дүн шинжилгээ бэлэн боллоо!")
                st.markdown(response.text)
                
            except Exception as e:
                st.error(f"AI холболтонд алдаа гарлаа: {e}")
    else:
        st.error("⚠️ Хөтөч ажиллуулахын тулд нэр болон сонирхлоо заавал бичнэ үү!")

