import streamlit as st

# 1. Вэбсайтны толгой хэсэг болон гарчиг
st.set_page_config(page_title="AI Career Guide", page_icon="🚀")
st.title("🎯 Ирээдүйн Мэргэжил Сонголтын AI Туслах")
st.write("Танд хамгийн тохирох, ирээдүйтэй мэргэжлийг олоход тусална.")

st.divider() # Хөндлөн зураас

# 2. Хэрэглэгчээс мэдээлэл авах цагаан нүднүүд (Input болон Dropdown)
ner = st.text_input("Таны нэр хэн бэ?", placeholder="Энд нэрээ бичнэ үү...")

# Салбаруудыг доошоо урсдаг цэснээс сонгодог болгов
sonirhol = st.selectbox(
    "Та ямар салбарыг сонирхож байна вэ?",
    ["Сонгох...", "технологи", "урлаг"]
)

# 3. Товчлуур дарах үед ажиллах тархи
if st.button("Ирээдүйг шинжлэх 🧠"):
    if ner and sonirhol != "Сонгох...":
        st.success(f"👋 Уулзсандаа баяртай, {ner}!")
        st.subheader(f"✨ Хоёулаа '{sonirhol}' салбарын ирээдүйг шинжилж үзье:")
        
        if sonirhol == "урлаг":
            st.info("👉 *Ирээдүйд эрэлттэй мэргэжил:* Дижитал дизайнер / AI Уран бүтээлч")
            st.warning("👉 *AI-д орлуулагдах магадлал:* 15% (Хүний мэдрэмж хэрэгтэй)")
            st.success("💰 *Дундаж цалин:* $5,000/сар")
            
        elif sonirhol == "технологи":
            st.info("👉 *Ирээдүйд эрэлттэй мэргэжил:* Хиймэл оюун ухааны инженер")
            st.warning("👉 *AI-д орлуулагдах магадлал:* 5%")
            st.success("💰 *Дундаж цалин:* $9,000/сар")
    else:
        st.error("⚠️ Хөтөч ажиллуулахын тулд нэрээ бичиж, салбараа сонгоно уу!")
